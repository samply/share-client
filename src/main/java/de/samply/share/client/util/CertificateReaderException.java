package de.samply.share.client.util;

public class CertificateReaderException extends Throwable {


  public CertificateReaderException() {
  }

  public CertificateReaderException(String message) {
    super(message);
  }

  public CertificateReaderException(String message, Throwable cause) {
    super(message, cause);
  }

  public CertificateReaderException(Throwable cause) {
    super(cause);
  }
}
